/**
 * op_add - addition
 * @a: premier nombre
 * @b: deuxième nombre
 * Return: a + b
 */
int op_add(int a, int b)
{
    return (a + b);
}

/**
 * op_sub - soustraction
 * @a: premier
 * @b: deuxième
 * Return: a - b
 */
int op_sub(int a, int b)
{
    return (a - b);
}

/**
 * op_mul - multiplication
 * @a: premier
 * @b: deuxième
 * Return: a * b
 */
int op_mul(int a, int b)
{
    return (a * b);
}

/**
 * op_div - division
 * @a: dividende
 * @b: diviseur
 * Return: a / b
 */
int op_div(int a, int b)
{
    return (a / b);
}

/**
 * op_mod - modulo
 * @a: nombre
 * @b: diviseur
 * Return: reste de la division
 */
int op_mod(int a, int b)
{
    return (a % b);
}
